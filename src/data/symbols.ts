// Function to search symbols
export function searchSymbols(query: string): DreamSymbol[] {
    const lowercaseQuery = query.toLowerCase();
    return dreamSymbols.filter(symbol =>
        symbol.name.includes(query) ||
        symbol.category.includes(lowercaseQuery) ||
        symbol.relatedSymbols.some(rs => rs.includes(query))
    );
}

// Function to get symbol by ID
export function getSymbolById(id: string): DreamSymbol | undefined {
    return dreamSymbols.find(symbol => symbol.id === id);
}

// Function to get symbols by category
export function getSymbolsByCategory(categoryId: string): DreamSymbol[] {
    return dreamSymbols.filter(symbol => symbol.category === categoryId);
}
